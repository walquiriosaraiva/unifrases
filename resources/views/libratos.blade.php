                        <p class="text-left">
                            <strong>2) A FONÉTICA é convencionada da seguinte forma:</strong><br/><br/>
                            1 = EI.<br/>
                            2 = BU.<br/>
                            3 = AH.<br/>
                            4 = OI.<br/>
                            5 = OH.<br/>
                            6 = LE.<br/>
                            7 = MO.<br/>
                            8 = EH.<br/>
                            9 = LI.<br/>
                            0 = AI.<br/>
                            / = LA. (Caso Especial).<br/>
                        </p>

                        <p class="text-justify">
                            Obs: a) a partir desta FONÉTICA (Sílabas!), qualquer pessoa com os números da Sequencia
                            Numérica (SN) pode falar todo o Banco de Dados. b) a pronúncia desta FONÉTICA é feita sempre
                            com o som aberto, ou seja, as sílabas devem ser pronunciadas de forma clara e limpas, sem
                            carregar ou fazer som fechado, justamente para facilitar o sotaque a qualquer povo do mundo.
                        </p>

                        <p class="text-justify">
                            Convenção: para dois (02) números iguais, em uma mesma Sequencia Numérica pode-se falar o BI
                            precedido do Fonema, por exemplo:
                        </p>

                        <p class="text-justify">
                            Ex: 677 = LE BI MO ou LE MO MO.
                        </p>

                        <p class="text-justify">
                            Convenção: para três (03) números iguais, em uma mesma Sequencia Numérica, fala-se o TRI
                            precedido do fonema, por exemplo:
                        </p>

                        <p class="text-justify">
                            Ex: 999 = TRI LI ou LI LI LI.
                        </p>

                        <p class="text-justify">
                            Falando/Pronunciando: 999/1= TRI . LI LA EI.<br/>
                            Convenção: para quatro (04) números iguais, em uma mesma Sequencia
                            Numérica, fala-se TETRA precedido do fonema, por exemplo:<br/>
                            Ex: 7777 = TETRA MO ou MO MO MO MO.<br/>
                            Falando/Pronunciando: 7777.9999 = TETRA . MO TETRA LI.<br/>
                            /00.00.00.00.00.01/ = LA OH BI . AI EI LA.<br/>
                            /000.000.000.001/ = LA . TRI AI BI . AI EI LA.<br/>
                            /0000.0000. 0000.0001/ = LA . TETRA AI TRI . AI EI LA.<br/>
                            /00.00.28.08.1955/ = LA. BI AI. BU EH AI. EH EI LI . BI OH LA.<br/>
                            BI = Duas unidades juntas (00).<br/>
                            TRI = Três unidades juntas (000).<br/>
                            TETRA = Quatro unidades juntas (0000).<br/>
                            PENTA = Cinco unidades juntas (00000).<br/>
                        </p>

                        <p class="text-justify">
                            <strong>IMPORTANTE para a Metodologia Fonética:</strong> Sempre que um número preceder um
                            indicativo de repetição (BI, TRI TETRA ou PENTA), tantas repetiççes serão consideradas para
                            minimizar as mesmas repetiççes, ou seja, assim se faz a redução da Fonética para melhorar o
                            falar propriamente dito. Basta observar os exemplos acima demonstrados.<br/>
                        </p>

                        <p class="text-justify">
                            <strong>3) Metodologia Gesticular (para pessoas com deficiência auditiva)</strong>, para uso
                            linguístico da UniFrases - Linguagem Universal que, poderá ser usado tal qual como a língua
